
public class Sample05_operator {

	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 3;
		
		System.out.println("n1 + n2 = " + (n1 + n2));
		System.out.println("n1 - n2 = " + (n1 - n2));
		System.out.println("n1 * n2 = " + (n1 * n2));
		System.out.println("n1 / n2 = " + (n1 / n2));
		System.out.println("n1 / 3.0 = " + (n1 / 3.0));
		System.out.println("n1 % n2 = " + (n1 % n2));
	}

}
